"""
Lists are a collection of data
"""


my_list = [80, 96, 72, 100, 8]
print(my_list)
my_list.append(1000) # Add an item to the end of the list
print(my_list)
my_list.insert(2, 1000) # Add an item at a specific index (so here after 72)
print(my_list)
my_list.remove(8) # Remove an item from the list
print(my_list)
my_list.pop(0) # Remove an item at a specific index
print(my_list)
my_list.sort() 
print(my_list)









