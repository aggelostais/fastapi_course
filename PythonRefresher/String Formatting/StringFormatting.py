"""
String Formatting
"""


sentence = "Hi {} {}" # {} is a place holder
first_name = "Eric"
last_name = "Roby"
print(sentence.format(first_name, last_name)) # .format() is a method that replaces the place holders with the arguments passed to it


print(f"Hi {first_name} {last_name} I hope you are learning") # f-string





